export function getDefaultWords(): WordItem[] {
  const list: WordItem[] = [];
  for (const raw of RAW_WORDS) {
    const parts = raw.split("|");
    if (parts.length < 2) continue;
    const category = parts[0];
    const itemsStr = parts[1];
    const items = itemsStr.split(",");
    for (const item of items) {
      const pair = item.split(":");
      if (pair.length < 2) continue;
      const word = pair[0].trim();
      const emoji = pair[1].trim();
      if (word) {
        list.push({ word, category, emoji });
      }
    }
  }
  return list;
}

export function loadWords(): WordItem[] {
  const defaults = getDefaultWords();
  try {
    const customStr = localStorage.getItem("custom_taboo_words");
    if (customStr) {
      const customs: WordItem[] = JSON.parse(customStr);
      // Mark as custom
      const customsWithFlag = customs.map(c => ({ ...c, isCustom: true }));
      return [...defaults, ...customsWithFlag];
    }
  } catch (e) {
    console.error("Error loading custom words from localStorage:", e);
  }
  return defaults;
}

export function saveCustomWords(customs: WordItem[]): void {
  try {
    localStorage.setItem("custom_taboo_words", JSON.stringify(customs));
  } catch (e) {
    console.error("Error saving custom words to localStorage:", e);
  }
}
